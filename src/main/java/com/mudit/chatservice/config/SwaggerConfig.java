package com.mudit.chatservice.config;

import io.swagger.v3.oas.models.Components;
import io.swagger.v3.oas.models.OpenAPI;
import io.swagger.v3.oas.models.security.OAuthFlow;
import io.swagger.v3.oas.models.security.OAuthFlows;
import io.swagger.v3.oas.models.security.SecurityRequirement;
import io.swagger.v3.oas.models.security.SecurityScheme;
import io.swagger.v3.oas.models.servers.Server;
import org.springframework.boot.context.properties.EnableConfigurationProperties;
import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;

import java.util.List;

@Configuration
@EnableConfigurationProperties(SwaggerOAuthProperties.class)
public class SwaggerConfig {

    @Bean
    public OpenAPI openAPI(SwaggerOAuthProperties swaggerOAuthProperties) {
        final String securitySchemeName = "oauth2";

        return new OpenAPI()
                .servers(List.of(new Server().url("/")))

                .addSecurityItem(new SecurityRequirement()
                        .addList(securitySchemeName))
                .components(new Components()
                        .addSecuritySchemes(securitySchemeName,
                                new SecurityScheme()
                                        .type(SecurityScheme.Type.OAUTH2)
                                        .flows(new OAuthFlows()
                                                .authorizationCode(new OAuthFlow()
                                                        .authorizationUrl(swaggerOAuthProperties.authorizationUrl())
                                                        .tokenUrl(swaggerOAuthProperties.tokenUrl())))));
    }
}
